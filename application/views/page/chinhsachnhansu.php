        <strong><img alt="" src="<?php echo base_url()?>public/image/khenthuong.jpg" class="content1" />3. Hệ thống lương - thưởng</strong>
      </p>
      <ul>
        <li>Tăng lương: Định kỳ xét tăng lương theo định kỳ 1 năm/lần. Thời gian xét tăng lương từ đầu năm. Tỷ lệ xét tăng lương dựa trên hiệu quả làm việc của năm trước đó</li>
      </ul>
      <ul>
        <li>Thưởng quý: Hằng quý dựa trên kết quả đánh giá KPI, công ty sẽ khen thưởng cho các cá nhân và tập thể có thành tích làm việc tốt, ngoài ra tập đoàn còn vinh danh và bình chọn các cá nhân và tập thể để vinh danh và trao bằng khen cho các thành tích trong kỳ.</li>
        <li>Thưởng năm: Đất Xanh luôn luôn đứng đầu so với các Công ty cùng ngành nghề về các khoản thưởng cuối năm. Liên tục từ các năm 2014-2016 mỗi CBNV được thưởng từ 3-8 tháng lương vào cuối năm</li>
        <li>Thưởng ESOP: Đối với cấp quản lý của tập đoàn từ cấp 4 (Trưởng Bộ phận trở lên) hằng năm đều có chính sách thưởng cổ phiếu theo thâm niên, thang cấp bậc và hiệu quả làm việc. Đây các các khoản thưởng có giá trị lớn giúp CBNV có thể tích lũy và yên tâm đóng góp lâu dài cho tập đoàn</li>
        <li>Thưởng vượt chỉ tiêu: Hằng năm ban điều hành sẽ được thêm một khoản thưởng vượt chỉ tiêu lợi nhuận căn cứ trên tỷ lệ được đại hội đồng cổ đông thông qua, đây cũng là sự ghi nhận sự cống hiến và nổ lực của tập thể quản lý Đất Xanh trong công tác quản trị hướng đến hiệu quả vượt mong đợi của cổ đông.</li>
      </ul>
